CODE  SEGMENT
MAIN PROC FAR
ASSUME CS:CODE
	START : PUSH DS
	SUB AX,AX
	PUSH AX
	MOV BH,0;统计数字的个数
	MOV BL,0;统计字母的个数
	MOV DH,0;统计其它字符的个数
	MOV CX,10

AA1:
	CMP CX,0
	JZ  STOP  ;输入字符超过10个
	MOV AH,1
	INT 21H   ;从键盘接收一个字符
	CMP AL,20H
	JZ  STOP
	CMP AL,'0'
	JB  CC1
	CMP AL,'9'
	JA  CC1
	INC BH
	DEC CX
	JMP AA1

CC1:
	CMP AL,'A'
	JB CC2
	CMP AL,'z'
	JA CC2
	CMP AL,'Z'
	JNGE CN
	CMP AL,'a'
	JNBE CN
	JMP CC2
	
CN: INC BL
	DEC CX
	JMP AA1

CC2:
	INC DH
	DEC CX
	JMP AA1

STOP:
	MOV AH,02
	ADD BH,30H
	MOV DL,BH
	INT 21H
	
	MOV AH,02 
	ADD BL,31H
	MOV DL,BL
	INT 21H
	
	MOV AH,02
	ADD DH,2FH
	MOV DL,DH
	INT 21H
	RET
MAIN ENDP
CODE ENDS
END